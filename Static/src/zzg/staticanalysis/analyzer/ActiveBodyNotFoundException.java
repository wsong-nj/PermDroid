package zzg.staticanalysis.analyzer;

public class ActiveBodyNotFoundException extends RuntimeException {

	public ActiveBodyNotFoundException(String arg0) {
		super(arg0);
	}

	public ActiveBodyNotFoundException(Throwable arg0) {
		super(arg0);
	}

	public ActiveBodyNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
